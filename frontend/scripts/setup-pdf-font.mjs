import { copyFileSync, existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs'
import { execSync } from 'node:child_process'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const targetDir = join(root, 'public', 'fonts')
const target = join(targetDir, 'SimHei.ttf')
const generatedTs = join(root, 'src/lib/inventory/pdfFontData.generated.ts')

const DOWNLOAD_URLS = [
  'https://raw.githubusercontent.com/notofonts/noto-cjk/main/Sans/TTF/TraditionalChinese/NotoSansTC-Regular.ttf',
  'https://github.com/notofonts/noto-cjk/raw/main/Sans/TTF/TraditionalChinese/NotoSansTC-Regular.ttf',
]

const TTF_SOURCES = [
  process.env.PDF_FONT_SOURCE,
  'C:\\Windows\\Fonts\\simhei.ttf',
  '/usr/share/fonts/truetype/noto/NotoSansTC-Regular.ttf',
  '/usr/share/fonts/truetype/noto/NotoSansCJK-Regular.ttf',
  '/usr/share/fonts/opentype/noto/NotoSansTC-Regular.otf',
]

const TTC_SOURCES = [
  '/usr/share/fonts/truetype/wqy/wqy-microhei.ttc',
  '/usr/share/fonts/truetype/wqy/wqy-zenhei.ttc',
]

function isValidTtf(filePath) {
  if (!existsSync(filePath) || !filePath.toLowerCase().endsWith('.ttf')) {
    return false
  }
  const header = readFileSync(filePath).subarray(0, 4)
  return header[0] === 0x00 && header[1] === 0x01 && header[2] === 0x00 && header[3] === 0x00
}

function discoverNotoTcTtf() {
  try {
    const result = execSync(
      'find /usr/share/fonts -iname "NotoSansTC-Regular.ttf" 2>/dev/null | head -n 1',
      { encoding: 'utf8' },
    ).trim()
    return result || null
  } catch {
    return null
  }
}

async function validateWithJsPdf(ttfPath) {
  const { jsPDF } = await import('jspdf')
  const base64 = readFileSync(ttfPath).toString('base64')
  const doc = new jsPDF()
  const vfsName = 'Validate.ttf'
  const family = 'ValidateFont'

  doc.addFileToVFS(vfsName, base64)
  doc.addFont(vfsName, family, 'normal')
  doc.setFont(family, 'normal')
  doc.text('測試中文', 10, 10)
  doc.getTextWidth('測試中文')

  const fonts = doc.getFontList()
  if (!fonts[family]) {
    throw new Error('jsPDF 未成功註冊字型')
  }
}

function writeGeneratedBase64() {
  const base64 = readFileSync(target).toString('base64')
  writeFileSync(
    generatedTs,
    `/** Auto-generated by scripts/setup-pdf-font.mjs — do not edit */\nexport const PDF_FONT_BASE64 = ${JSON.stringify(base64)}\n`,
  )
  console.log(`PDF 字型已嵌入：${generatedTs}`)
}

async function finalizeFont() {
  if (!isValidTtf(target)) {
    throw new Error('不是有效的 TTF 檔案')
  }
  await validateWithJsPdf(target)
  writeGeneratedBase64()
  console.log(`PDF 字型就緒：${target}`)
}

async function tryCopySource(source) {
  if (!source || !existsSync(source) || source === target) {
    return false
  }
  if (!isValidTtf(source)) {
    return false
  }
  copyFileSync(source, target)
  console.log(`PDF 字型已複製：${source} → ${target}`)
  await finalizeFont()
  return true
}

function extractTtcToTtf(ttcPath) {
  console.log(`正在從 TTC 提取 PDF 字型：${ttcPath}`)
  execSync(
    `python3 -c "from fontTools.ttLib import TTFont; import sys; TTFont(sys.argv[1], fontNumber=0).save(sys.argv[2])" "${ttcPath}" "${target}"`,
    { stdio: 'inherit' },
  )
}

async function downloadTtf() {
  for (const url of DOWNLOAD_URLS) {
    console.log(`正在下載 PDF 字型：${url}`)
    try {
      const response = await fetch(url, {
        headers: {
          'User-Agent': 'Toolbox-pdf-font-setup/1.0',
          Accept: '*/*',
        },
        redirect: 'follow',
      })
      if (!response.ok) {
        console.warn(`下載失敗（HTTP ${response.status}）：${url}`)
        continue
      }

      const buffer = Buffer.from(await response.arrayBuffer())
      if (buffer.length < 1024 || buffer[0] !== 0x00 || buffer[1] !== 0x01) {
        console.warn(`下載內容無效：${url}`)
        continue
      }

      writeFileSync(target, buffer)
      console.log(`PDF 字型已下載：${target}（${(buffer.length / 1024 / 1024).toFixed(1)} MB）`)
      await finalizeFont()
      return true
    } catch (error) {
      console.warn(`下載或驗證失敗：${url}`, error)
    }
  }
  return false
}

mkdirSync(targetDir, { recursive: true })

if (existsSync(target) && isValidTtf(target)) {
  try {
    await finalizeFont()
    console.log(`PDF 字型已存在且通過 jsPDF 驗證：${target}`)
    process.exit(0)
  } catch (error) {
    console.warn('現有 PDF 字型無法用於 jsPDF，將重新產生…', error)
  }
}

const discoveredNoto = discoverNotoTcTtf()
const allTtfSources = discoveredNoto ? [...TTF_SOURCES, discoveredNoto] : TTF_SOURCES

for (const source of allTtfSources) {
  try {
    if (await tryCopySource(source)) {
      process.exit(0)
    }
  } catch (error) {
    console.warn(`來源不可用：${source}`, error)
  }
}

if (await downloadTtf()) {
  process.exit(0)
}

for (const ttcPath of TTC_SOURCES) {
  if (!existsSync(ttcPath)) {
    continue
  }
  try {
    extractTtcToTtf(ttcPath)
    await finalizeFont()
    process.exit(0)
  } catch (error) {
    console.warn(`TTC 提取或驗證失敗：${ttcPath}`, error)
  }
}

console.error(
  '無法準備 jsPDF 可用的 PDF 字型。Docker 請安裝 fonts-noto-extra；Windows 請執行 npm run setup:pdf-font。',
)
process.exit(1)
